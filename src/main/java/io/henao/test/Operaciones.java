public class Operaciones {
    public int sumar(int a, int b){
        return a + b;
    }

    public int resta(int c, int d){
        return c - d;
    }

    public int multiplicar(int e, int f){
        return e * f;
    }

    public int dividir(int numG, int numH){
        return numG / numH;
    }
}
